#include <iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout << "Enter number 1: ";
    cin >> num1;
    cout << "Enter number 2: ";
    cin >> num2;
    cout << "The sum is " << num1 + num2 << endl;
    cout << "The difference is " << num1 - num2 << endl;
    cout << "The product is " << num1 * num2 << endl;
    cout << "The division is " << num1 / num2 << endl;
    return 0;
}
